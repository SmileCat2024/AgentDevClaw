// ── initializeAgentCreatorWorkspace (integration) ─────────────────────────────

describe('initializeAgentCreatorWorkspace', () => {
  let tempRoot;

  beforeEach(() => {
    tempRoot = mkdtempSync(join(tmpdir(), 'claw-agent-init-'));
  });

  afterEach(() => {
    rmSync(tempRoot, { recursive: true, force: true });
  });

  it('creates all expected files in the output directory', async () => {
    const result = await initializeAgentCreatorWorkspace('test-agent', tempRoot, 'My goal');

    assert.strictEqual(result.agentName, 'test-agent');
    assert.strictEqual(result.parentDir, tempRoot);

    // Verify all files exist
    for (const file of result.files) {
      const filePath = join(result.outputDir, file);
      const stat = statSync(filePath);
      assert.ok(stat.isFile(), `Expected file to exist: ${file}`);
    }

    // Verify .agentdev/prompts/system.md specifically (nested path)
    const promptPath = join(result.outputDir, '.agentdev', 'prompts', 'system.md');
    assert.ok(statSync(promptPath).isFile(), 'system.md should exist');
  });

  it('writes correct package.json content', async () => {
    const result = await initializeAgentCreatorWorkspace('pkg-test', tempRoot, 'goal');
    const pkg = JSON.parse(readFileSync(join(result.outputDir, 'package.json'), 'utf8'));
    assert.strictEqual(pkg.name, '@local/pkg-test');
    assert.strictEqual(pkg.type, 'module');
    assert.ok(pkg.dependencies.agentdev);
  });

  it('writes correct metadata.json content', async () => {
    const result = await initializeAgentCreatorWorkspace('meta-test', tempRoot, 'Special goal');
    const meta = JSON.parse(readFileSync(join(result.outputDir, 'metadata.json'), 'utf8'));
    assert.strictEqual(meta.id, 'meta-test');
    assert.strictEqual(meta.name, 'MetaTest');
    assert.strictEqual(meta.description, 'Special goal');
    assert.deepStrictEqual(meta.features, ['todo', 'audit', 'shell', 'websearch', 'user-input', 'mcp', 'skill']);
  });

  it('writes agent.js with correct class name', async () => {
    const result = await initializeAgentCreatorWorkspace('code-test', tempRoot, 'goal');
    const src = readFileSync(join(result.outputDir, 'agent.js'), 'utf8');
    assert.ok(src.includes('class CodeTestAgent extends BasicAgent'));
  });

  it('writes system.md with goal', async () => {
    const result = await initializeAgentCreatorWorkspace('prompt-test', tempRoot, 'Be helpful');
    const prompt = readFileSync(join(result.outputDir, '.agentdev', 'prompts', 'system.md'), 'utf8');
    assert.ok(prompt.includes('# PromptTest'));
    assert.ok(prompt.includes('Be helpful'));
  });

  it('writes README.md', async () => {
    const result = await initializeAgentCreatorWorkspace('readme-test', tempRoot, 'goal');
    const readme = readFileSync(join(result.outputDir, 'README.md'), 'utf8');
    assert.ok(readme.includes('# readme-test'));
  });

  it('creates project docset directory structure', async () => {
    const result = await initializeAgentCreatorWorkspace('docset-test', tempRoot, 'goal');
    // PROJECT_DOCSET_SUBPATH = .agentdev/claw-workspace
    const docsetDir = join(result.outputDir, '.agentdev', 'claw-workspace');
    assert.ok(statSync(docsetDir).isDirectory(), 'project docset dir should exist');
    const projectFile = join(docsetDir, 'project.json');
    const projectRecord = JSON.parse(readFileSync(projectFile, 'utf8'));
    assert.strictEqual(projectRecord.workspaceId, 'agent-creator');
    assert.strictEqual(projectRecord.projectType, 'agent');
    assert.strictEqual(projectRecord.projectName, 'docset-test');
  });

  it('rejects invalid agent name with 400', async () => {
    await assert.rejects(
      () => initializeAgentCreatorWorkspace('Invalid Name!', tempRoot, 'goal'),
      (err) => {
        assert.strictEqual(err.statusCode, 400);
        assert.ok(err.message.includes('Invalid agent name'));
        return true;
      },
    );
  });

  it('rejects non-existent parent directory with 400', async () => {
    const fakeDir = join(tempRoot, 'does-not-exist');
    await assert.rejects(
      () => initializeAgentCreatorWorkspace('test-agent', fakeDir, 'goal'),
      (err) => {
        assert.strictEqual(err.statusCode, 400);
        assert.ok(err.message.includes('Parent directory does not exist'));
        return true;
      },
    );
  });

  it('rejects already-existing output directory with 409', async () => {
    // Pre-create the output dir
    mkdirSync(join(tempRoot, 'existing-agent'));
    await assert.rejects(
      () => initializeAgentCreatorWorkspace('existing-agent', tempRoot, 'goal'),
      (err) => {
        assert.strictEqual(err.statusCode, 409);
        assert.ok(err.message.includes('already exists'));
        return true;
      },
    );
  });

  it('rejects empty agent name', async () => {
    await assert.rejects(
      () => initializeAgentCreatorWorkspace('', tempRoot, 'goal'),
      (err) => {
        assert.strictEqual(err.statusCode, 400);
        return true;
      },
    );
  });

  it('handles missing goal gracefully', async () => {
    const result = await initializeAgentCreatorWorkspace('no-goal', tempRoot, '');
    assert.strictEqual(result.agentName, 'no-goal');
    // Files should still be created
    assert.ok(statSync(join(result.outputDir, 'agent.js')).isFile());
  });
});

