// ── buildGeneratedAgentClassName ──────────────────────────────────────────────

describe('buildGeneratedAgentClassName', () => {
  it('converts hyphenated name to PascalCase', () => {
    assert.strictEqual(buildGeneratedAgentClassName('my-cool-agent'), 'MyCoolAgent');
  });

  it('handles single word', () => {
    assert.strictEqual(buildGeneratedAgentClassName('bot'), 'Bot');
  });

  it('handles multi-hyphen', () => {
    assert.strictEqual(buildGeneratedAgentClassName('a-b-c-d'), 'ABCD');
  });

  it('falls back to GeneratedAgent for empty string', () => {
    assert.strictEqual(buildGeneratedAgentClassName(''), 'GeneratedAgent');
  });

  it('falls back to GeneratedAgent for null/undefined', () => {
    assert.strictEqual(buildGeneratedAgentClassName(null), 'GeneratedAgent');
    assert.strictEqual(buildGeneratedAgentClassName(undefined), 'GeneratedAgent');
  });

  it('handles leading/trailing hyphens', () => {
    assert.strictEqual(buildGeneratedAgentClassName('-test-'), 'Test');
  });

  it('handles numbers in name', () => {
    assert.strictEqual(buildGeneratedAgentClassName('bot-3000'), 'Bot3000');
  });
});

