// ── buildAgentWorkspaceMetadata ───────────────────────────────────────────────

describe('buildAgentWorkspaceMetadata', () => {
  it('returns complete metadata structure', () => {
    const meta = buildAgentWorkspaceMetadata('my-agent', 'Do stuff');
    assert.strictEqual(meta.id, 'my-agent');
    assert.strictEqual(meta.kind, 'agent');
    assert.strictEqual(meta.name, 'MyAgent');
    assert.strictEqual(meta.description, 'Do stuff');
    assert.strictEqual(meta.version, '0.1.0');
    assert.strictEqual(meta.icon, 'bot');
    assert.strictEqual(meta.category, 'custom');
    assert.strictEqual(meta.enabled, true);
  });

  it('includes all default features', () => {
    const meta = buildAgentWorkspaceMetadata('test', 'goal');
    const expectedFeatures = ['todo', 'audit', 'shell', 'websearch', 'user-input', 'mcp', 'skill'];
    assert.deepStrictEqual(meta.features, expectedFeatures);
  });

  it('includes UI entry and tabs', () => {
    const meta = buildAgentWorkspaceMetadata('test', 'goal');
    assert.strictEqual(meta.ui.entry, 'home');
    assert.ok(Array.isArray(meta.ui.tabs));
    assert.strictEqual(meta.ui.tabs.length, 2);
    const tabIds = meta.ui.tabs.map((t) => t.id);
    assert.ok(tabIds.includes('home'));
    assert.ok(tabIds.includes('chat'));
  });

  it('includes home blocks (hero + session-list)', () => {
    const meta = buildAgentWorkspaceMetadata('test', 'goal');
    assert.ok(Array.isArray(meta.ui.home.blocks));
    assert.strictEqual(meta.ui.home.blocks.length, 2);
    const blockIds = meta.ui.home.blocks.map((b) => b.id);
    assert.ok(blockIds.includes('hero'));
    assert.ok(blockIds.includes('session-list'));
  });

  it('uses goal in hero body', () => {
    const meta = buildAgentWorkspaceMetadata('bot', 'My specific goal');
    const hero = meta.ui.home.blocks.find((b) => b.id === 'hero');
    assert.strictEqual(hero.body.zh, 'My specific goal');
    assert.strictEqual(hero.body.en, 'My specific goal');
  });

  it('falls back to default description when no goal', () => {
    const meta = buildAgentWorkspaceMetadata('bot', '');
    assert.ok(meta.description.includes('generated by AgentDevClaw'));
    const hero = meta.ui.home.blocks.find((b) => b.id === 'hero');
    assert.ok(hero.body.zh.length > 0);
  });

  it('uses displayName for hero title', () => {
    const meta = buildAgentWorkspaceMetadata('cool-bot', 'goal');
    const hero = meta.ui.home.blocks.find((b) => b.id === 'hero');
    assert.strictEqual(hero.title.zh, 'CoolBot');
    assert.strictEqual(hero.title.en, 'CoolBot');
  });
});

