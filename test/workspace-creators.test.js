// ── setupWorkspaceCreatorRoutes ───────────────────────────────────────────────

describe('setupWorkspaceCreatorRoutes', () => {
  it('registers both creator routes on the app', () => {
    const registeredRoutes = [];

    const mockApp = {
      post(routePath, ...handlers) {
        registeredRoutes.push(routePath);
      },
    };

    const mockExpress = {
      json: () => (_req, _res, next) => next(),
    };

    setupWorkspaceCreatorRoutes(mockApp, mockExpress);

    assert.ok(registeredRoutes.includes('/protoclaw/feature_creator/initialize'));
    assert.ok(registeredRoutes.includes('/protoclaw/agent_creator/initialize'));
    assert.strictEqual(registeredRoutes.length, 2, 'should register exactly 2 routes');
  });

  it('route handlers call express.json() middleware', () => {
    let jsonMiddlewareCalled = false;

    const mockApp = {
      post(_routePath, middleware, _handler) {
        // Call the middleware to verify it's the express.json() result
        if (typeof middleware === 'function') {
          middleware({}, {}, () => {
            jsonMiddlewareCalled = true;
          });
        }
      },
    };

    const mockExpress = {
      json: () => (_req, _res, next) => next(),
    };

    setupWorkspaceCreatorRoutes(mockApp, mockExpress);

    // express.json() should have been called to produce the middleware
    assert.ok(jsonMiddlewareCalled, 'express.json() middleware should be invoked');
  });
});
